// Incluindo bibliotecas
#include "aritmetica.h"

// Função Soma
int soma (int a, int b) {
    return a + b;
}

// Função Subtração
int subtracao (int a, int b) {
    return a - b;
}

// Função Multiplicação
int multiplicacao (int a, int b) {
    return a * b;
}

// Função Divisão
int divisao (int a, int b) {
    return a / b;
}